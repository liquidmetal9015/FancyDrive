package org.usfirst.frc.team2682.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	

	//Right Encoder
	public static final int NCODER_1 = 8;
	public static final int NCODER_2 = 9;
	
	//Left Encoder
	public static final int NCODEL_1 = 6;
	public static final int NCODEL_2 = 7;

	
	//Drive Train
	public static final int RIGHT_MOTOR_PWM = 0;
	public static final int LEFT_MOTOR_PWM = 1;
	
}
